package com.rsip.mobile.Model;

public class KalendarHijriModel {

    String dateHijriah,dayHijriah,monthHijriah,yearHijriah,holidays;

    public KalendarHijriModel() {
    }

    public String getDateHijriah() {
        return dateHijriah;
    }

    public void setDateHijriah(String dateHijriah) {
        this.dateHijriah = dateHijriah;
    }

    public String getDayHijriah() {
        return dayHijriah;
    }

    public void setDayHijriah(String dayHijriah) {
        this.dayHijriah = dayHijriah;
    }

    public String getMonthHijriah() {
        return monthHijriah;
    }

    public void setMonthHijriah(String monthHijriah) {
        this.monthHijriah = monthHijriah;
    }

    public String getYearHijriah() {
        return yearHijriah;
    }

    public void setYearHijriah(String yearHijriah) {
        this.yearHijriah = yearHijriah;
    }

    public String getHolidays() {
        return holidays;
    }

    public void setHolidays(String holidays) {
        this.holidays = holidays;
    }
}
